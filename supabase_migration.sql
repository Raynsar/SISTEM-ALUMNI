-- ───────────────────────────────────────────────────────────────────
-- TABEL 1: alumni
-- Data master alumni (dari file Excel). Read-only dari web app.
-- ───────────────────────────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS alumni (
  id            BIGSERIAL    PRIMARY KEY,
  nama_lulusan  TEXT         NOT NULL,
  nim           TEXT         NOT NULL UNIQUE,
  tahun_masuk   INTEGER      NOT NULL,
  tanggal_lulus TEXT,
  fakultas      TEXT,
  program_studi TEXT,
  created_at    TIMESTAMPTZ  DEFAULT NOW()
);

-- Index
CREATE INDEX IF NOT EXISTS idx_alumni_nim          ON alumni (nim);
CREATE INDEX IF NOT EXISTS idx_alumni_nama         ON alumni (lower(nama_lulusan));
CREATE INDEX IF NOT EXISTS idx_alumni_fakultas     ON alumni (fakultas);
CREATE INDEX IF NOT EXISTS idx_alumni_prodi        ON alumni (program_studi);
CREATE INDEX IF NOT EXISTS idx_alumni_tahun_masuk  ON alumni (tahun_masuk);


-- ───────────────────────────────────────────────────────────────────
-- TABEL 2: alumni_profiles
-- Data profil yang diisi alumni sendiri via dashboard web.
-- ───────────────────────────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS alumni_profiles (
  id                   BIGSERIAL   PRIMARY KEY,
  nim                  TEXT        NOT NULL UNIQUE REFERENCES alumni(nim) ON DELETE CASCADE,

  -- Kontak Pribadi
  email                TEXT,
  phone                TEXT,

  -- Sosial Media Pribadi
  linkedin_username    TEXT,
  instagram_username   TEXT,
  facebook_username    TEXT,
  tiktok_username      TEXT,

  -- Pekerjaan
  job_status           TEXT,        -- PNS | Swasta | Wirausaha | BUMN | Lainnya
  workplace_name       TEXT,
  workplace_address    TEXT,
  position             TEXT,

  -- Sosial Media Tempat Bekerja
  company_linkedin     TEXT,
  company_instagram    TEXT,
  company_facebook     TEXT,
  company_tiktok       TEXT,

  -- Timestamps
  created_at           TIMESTAMPTZ DEFAULT NOW(),
  updated_at           TIMESTAMPTZ DEFAULT NOW()
);

-- Index
CREATE INDEX IF NOT EXISTS idx_profiles_nim       ON alumni_profiles (nim);
CREATE INDEX IF NOT EXISTS idx_profiles_status    ON alumni_profiles (job_status);
CREATE INDEX IF NOT EXISTS idx_profiles_workplace ON alumni_profiles (workplace_name);

-- Auto-update updated_at
CREATE OR REPLACE FUNCTION update_updated_at()
RETURNS TRIGGER AS $$
BEGIN
  NEW.updated_at = NOW();
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

DROP TRIGGER IF EXISTS trg_profiles_updated_at ON alumni_profiles;
CREATE TRIGGER trg_profiles_updated_at
  BEFORE UPDATE ON alumni_profiles
  FOR EACH ROW EXECUTE FUNCTION update_updated_at();


-- ───────────────────────────────────────────────────────────────────
-- ROW LEVEL SECURITY (RLS)
