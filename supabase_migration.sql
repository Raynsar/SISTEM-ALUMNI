-- Policy: semua orang (anon) bisa SELECT tabel alumni (untuk login)
CREATE POLICY "Public read alumni"
  ON alumni FOR SELECT
  TO anon
  USING (true);

-- Policy: semua orang (anon) bisa SELECT & UPSERT profil
-- (Untuk keamanan lebih tinggi, ganti anon → authenticated dan pakai Supabase Auth)
CREATE POLICY "Public read profiles"
  ON alumni_profiles FOR SELECT
  TO anon
  USING (true);

CREATE POLICY "Public upsert profiles"
  ON alumni_profiles FOR INSERT
  TO anon
  WITH CHECK (true);

CREATE POLICY "Public update profiles"
  ON alumni_profiles FOR UPDATE
  TO anon
  USING (true);


